#include <stdio.h>
/*
I am introducing some basic types of variable.
*/
int	main(void)
{
	char chr = 'C';
	int i = 50;
	float flt = 3.3333;

	printf("This is an Charecter type variable: %c.\n", chr);
	printf("This is an Integer type variable: %d.\n", i);
	printf("This is an Float type variable: %f.\n\n", flt);

	return (0);
}